              <p>Fred Morrison discovered a market for the modern-day flying disc[6] in 1938 when he and future wife, Lucile, were offered 25 cents for a cake pan that they were tossing back and forth on a beach in New Haven, CT. "That got the wheels turning, because you could buy a cake pan for five cents, and if people on the beach were willing to pay a quarter for it, well - there was a business," Morrison told The Virginian-Pilot newspaper in 2007.[7]</p>
            </div>

    	      <p>The Morrisons continued their business until World War II, when Morrison served in the Army Air Force, flying P-47s, and then was a prisoner of war. Mustered out, Morrison sketched a design for an aerodynamically improved flying disc that he called the Whirlo-Way. By 1948, after design modifications and experimentation with several prototypes, Morrison and business partner Warren Franscioni began producing the first plastic discs, renaming them the Flyin-Saucer in the wake of reported unidentified flying object-sightings.</p>

    	      <p>"We worked fairs, demonstrating it," Morrison told the Virginian-Pilot. The two of them once overheard someone saying the pair were using wires to make the discs hover, so they developed a sales pitch: "The Flyin-Saucer is free, but the invisible wire is $1." "That's where we learned we could sell these things," he said, because people were enthusiastic about them.</p>

    	      <p>Morrison and Franscioni ended their partnership in early 1950, and in 1954 Morrison formed his own company, called American Trends, to buy and sell Flyin-Saucers, which were by then being made of a flexible polypropylene plastic from Southern Connecticut Plastics, the original molder. After learning that he could produce his own disc more cheaply, in 1955 Morrison designed a new model, the Pluto Platter, the archetype of all modern flying discs. He sold the rights to Wham-O on January 23, 1957, and in 1958 Morrison was awarded U.S. Design Patent D183,626 for his product.</p>

            <p>In June 1957, Wham-O co-founder Richard Knerr decided to stimulate sales by giving the discs the additional brand name Frisbee (pronounced "friz'-bee"), after learning that Northeastern college students were calling the Pluto Platter by that name,[8] the term "Frisbee" coming from the name of the Connecticut-based pie manufacturer, Frisbie Pie Company,[9] which supplied pies to Yale University, where students started the campus craze by tossing empty pie tins stamped with the company's logo. "I thought the name was a horror. Terrible," Morrison told The Press-Enterprise of Riverside in 2007.[10] In 1982, Morrison told Forbes magazine that he had received about US$2 million in royalty payments and said: "I wouldn't change the name of it for the world."[10]</p>


