			<h2 id="frisbee">Frisbee</h2>
		
			<article>
            <h3>Brief</h3>
            <div class = "first-letter">
              <p>A frisbee (sometimes called a flying disc) is a disc-shaped gliding toy or sporting item that is generally plastic and roughly 20 to 25 centimetres (8 to 10 in) in diameter with a lip,[1] used recreationally and competitively for throwing and catching, for example, in flying disc games. The shape of the disc, an airfoil in cross-section, allows it to fly by generating lift as it moves through the air while spinning.</p>
            </div>

    	      <p>Frisbees are thrown and caught for free-form (freestyle) recreation and as part of many flying disc games. A wide range of frisbees variants are available commercially. Disc golf discs are usually smaller but denser and tailored for particular flight profiles to increase/decrease stability and distance. The longest recorded disc throw is by Simon Lizotte with a distance of 263.2 meters.[2] Disc dog sports use relatively slow flying discs made of more pliable material to better resist a dog's bite and prevent injury to the dog. Flying rings are also available; they typically travel significantly farther than any traditional flying disc. There are also illuminated discs meant for nighttime play — they are made of a phosphorescent plastic or contain battery-powered light-emitting diodes or chemiluminescent glowing sticks. Others whistle when they reach a certain velocity in flight.</p>

            <p>Lift is generated in the same way as a traditional airfoil. The rotating frisbee has a nearly vertical angular momentum vector, stabilizing its angle of attack via gyroscopic action. If the disc were not spinning, it would crash to pitch. When the disc is spinning, however, aerodynamic torque instead leads to precess about the spin axis, causing its trajectory to curve to the left or the right. Most discs are designed to be aerodynamically stable so that this roll is accurate for a fairly broad range of velocities and rates of spin. Many disc golf discs, however, are intentionally designed to be unstable. Higher rates of spin lead to more stability, and, for a given rate of spin, there is generally a range of velocities that are stable. Even a slight deformation in a disc (called a "taco," which in extreme cases looks like a taco shell) can cause negative effects when throwing long range. A disk can be checked for these deformations by holding it horizontally at eye level and looking at the rim while slowly turning it.[3]</p>

