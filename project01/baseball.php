            <div class = "first-letter">
              <p>The evolution of baseball from older bat-and-ball games is difficult to trace with precision. A French manuscript from 1344 contains an illustration of clerics playing a game, possibly la soule, with similarities to baseball.[1] Other old French games such as thèque, la balle au bâton, and la balle empoisonnée also appear to be related.[2] Consensus once held that today's baseball is a North American development from the older game rounders, popular in Great Britain and Ireland. Baseball Before We Knew It: A Search for the Roots of the Game (2005), by David Block, suggests that the game originated in England; recently uncovered historical evidence supports this position. Block argues that rounders and early baseball were actually regional variants of each other, and that the game's most direct antecedents are the English games of stoolball and "tut-ball."[3] It has long been believed that cricket also descended from such games, though evidence uncovered in early 2009 suggests that cricket may have been imported to England from Flanders.[4]</p>
            </div>

            <p>The earliest known reference to baseball is in a 1744 British publication, A Little Pretty Pocket-Book, by John Newbery. It contains a rhymed description of "base-ball" and a woodcut that shows a field set-up somewhat similar to the modern game—though in a triangular rather than diamond configuration, and with posts instead of ground-level bases.[5] David Block discovered that the first recorded game of "Bass-Ball" took place in 1749 in Surrey, and featured the Prince of Wales as a player.[6] William Bray, an English lawyer, recorded a game of baseball on Easter Monday 1755 in Guildford, Surrey.[7] This early form of the game was apparently brought to Canada by English immigrants. Rounders was also brought to the USA by Canadians of both British and Irish ancestry. The first known American reference to baseball appears in a 1791 Pittsfield, Massachusetts, town bylaw prohibiting the playing of the game near the town's new meeting house.[8] By 1796, a version of the game was well-known enough to earn a mention in a German scholar's book on popular pastimes. As described by Johann Gutsmuths, "englische Base-ball" involved a contest between two teams, in which "the batter has three attempts to hit the ball while at the home plate." Only one out was required to retire a side.[9]</p>

            <h3>Baseball around the world</h3>

            <div class = "first-letter">
              <p>Baseball, widely known as America's pastime, is well established in several other countries as well. The history of baseball in Canada has remained closely linked with that of the sport in the United States. As early as 1877, a professional league, the International Association, featured teams from both countries.[75] While baseball is widely played in Canada and many minor league teams have been based in the country, the American major leagues did not include a Canadian club until 1969, when the Montreal Expos joined the National League as an expansion team. In 1977, the expansion Toronto Blue Jays joined the American League. The Blue Jays won the World Series in 1992 and 1993, the first and still the only club from outside the United States to do so. After the 2004 season, Major League Baseball relocated the Expos to Washington, D.C., where the team is now known as the Nationals.</p>
            </div>

            <p>In 1847, American soldiers played what may have been the first baseball game in Mexico at Parque Los Berros in Xalapa, Veracruz. A few days after the Battle of Cerro Gordo, they used the "wooden leg captured (by the Fourth Illinois regiment) from General Santa Anna".[76] The first formal baseball league outside of the United States and Canada was founded in 1878 in Cuba, which maintains a rich baseball tradition and whose national team has been one of the world's strongest since international play began in the late 1930s (all organized baseball in the country has officially been amateur since the Cuban Revolution). The Dominican Republic held its first islandwide championship tournament in 1912.[77] Professional baseball tournaments and leagues began to form in other countries between the world wars, including the Netherlands (formed in 1922), Australia (1934), Japan (1936), Mexico (1937), and Puerto Rico (1938).[78] The Japanese major leagues—the Central League and Pacific League—have long been considered the highest quality professional circuits outside of the United States.[79] Japan has a professional minor league system as well, though it is much smaller than the American version—each team has only one farm club in contrast to MLB teams' four or five.[80]</p>

            <h3>Rules and Gameplay</h3>

            <div class = "first-letter">
              <p>A game is played between two teams, each composed of nine players, that take turns playing offense (batting and baserunning) and defense (pitching and fielding). A pair of turns, one at bat and one in the field, by each team constitutes an inning. A game consists of nine innings (seven innings at the high school level and in doubleheaders in college and minor leagues). One team—customarily the visiting team—bats in the top, or first half, of every inning. The other team—customarily the home team—bats in the bottom, or second half, of every inning. The goal of the game is to score more points (runs) than the other team. The players on the team at bat attempt to score runs by circling or completing a tour of the four bases set at the corners of the square-shaped baseball diamond. A player bats at home plate and must proceed counterclockwise to first base, second base, third base, and back home in order to score a run. The team in the field attempts both to prevent runs from scoring and to record outs, which remove opposing players from offensive action until their turn in their team's batting order comes up again. When three outs are recorded, the teams switch roles for the next half-inning. If the score of the game is tied after nine innings, extra innings are played to resolve the contest. Many amateur games, particularly unorganized ones, involve different numbers of players and innings.[92]</p>
            </div>

              <p>The game is played on a field whose primary boundaries, the foul lines, extend forward from home plate at 45-degree angles. The 90-degree area within the foul lines is referred to as fair territory; the 270-degree area outside them is foul territory. The part of the field enclosed by the bases and several yards beyond them is the infield; the area farther beyond the infield is the outfield. In the middle of the infield is a raised pitcher's mound, with a rectangular rubber plate (the rubber) at its center. The outer boundary of the outfield is typically demarcated by a raised fence, which may be of any material and height (many amateur games are played on unfenced fields). Fair territory between home plate and the outfield boundary is baseball's field of play, though significant events can take place in foul territory, as well.[93]</p>
