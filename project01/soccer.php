				<h3>Gameplay</h3>
				<div class = "first-letter">
					<p> Association football is played in accordance with a set of rules known as the Laws of the Game. The game is played using a spherical ball of 68.5–69.5 cm (27.0–27.4 in) circumference,[64] known as the football (or soccer ball). Two teams of eleven players each compete to get the ball into the other team's goal (between the posts and under the bar), thereby scoring a goal. The team that has scored more goals at the end of the game is the winner; if both teams have scored an equal number of goals then the game is a draw. Each team is led by a captain who has only one official responsibility as mandated by the Laws of the Game: to represent his or her team in the coin toss prior to kick-off or penalty kicks.[65]</p>
				</div>

				<p>The primary law is that players other than goalkeepers may not deliberately handle the ball with their hands or arms during play, though they do use their hands during a throw-in restart. Although players usually use their feet to move the ball around, they may use any part of their body (notably, "heading" with the forehead)[66] other than their hands or arms.[67] Within normal play, all players are free to play the ball in any direction and move throughout the pitch, though the ball cannot be received in an offside position.[68]</p>
